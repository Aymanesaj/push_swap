#include "libftprintf.h"

int	ft_gothrough(const char *str, int *i, char *c)
{
	while ((str[(*i)] == '.' || str[(*i)] == '0') && str[(*i)])
	{
		if (*c == '0' && str[*i] == '-')
			return (-1);
		if (*c == '0' && str[*i] == '.')
			*c = '.';
		(*i)++;
	}
	return (0);
}
