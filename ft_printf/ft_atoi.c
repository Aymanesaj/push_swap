#include "libftprintf.h"

long	ft_atoi(const char *str, int *i)
{
	long	len;

	len = 0;
	while (ft_isdigit(str[(*i)]))
	{
		len = len * 10 + (str[(*i)] - '0');
		(*i)++;
	}
	return (len);
}
