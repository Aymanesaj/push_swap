#include "libftprintf.h"

int	ft_strx(unsigned int nbr)
{
	unsigned long	nb;
	int				i;

	i = 1;
	nb = nbr;
	while (nb >= 16)
	{
		nb = nb / 16;
		i++;
	}
	return (i);
}
